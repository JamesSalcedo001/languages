# control flow



number = 10

# if else statement

if number > 5
    puts "Greater than 5"
else
    puts "5 or less"
end



# ternary operator 

result = number > 5 ? "Greater than 5" : "5 or less"
puts result




# loops


# for loop 

for i in 0...5
    puts i
end



#while loop 

j = 0

while j < 5
    puts j
    j += 1
end



# do while loop 


k = 0

begin
    puts k
    k += 1
end while k < 5